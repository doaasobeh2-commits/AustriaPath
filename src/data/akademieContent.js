const LEVEL_MAP = {
  A2: a2Akademie,
  B1: b1Akademie,
  B2: b2Akademie,
};

export function getAkademieLevelData(level) {
  const key = String(level || "A2").toUpperCase();
  const base = LEVEL_MAP[key] || a2Akademie;
  const expressCards =
    key === "B2" ? b2ExpressCards : key === "B1" ? b1ExpressCards : [];

  return {
    ...base,
    expressCards,
  };
}

/** Merge static akademie arrays with extracted model data; dedupe case-insensitively. */
export function mergeAkademieLists(staticList = [], dynamicList = []) {
  const map = new Map();
  [...staticList, ...dynamicList].forEach((item) => {
    const value = String(item || "").trim();
    if (!value) return;
    const dedupeKey = value.toLowerCase();
    if (!map.has(dedupeKey)) map.set(dedupeKey, value);
  });
  return [...map.values()];
}
